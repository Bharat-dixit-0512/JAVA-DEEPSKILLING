package Week2_dataStructure_and_algotrithm.Ex5_TaskManagementSystem;
public class Task {

    int taskId;
    String taskName;
    String status;
    Task next;

    public Task(int taskId, String taskName, String status) {
        this.taskId = taskId;
        this.taskName = taskName;
        this.status = status;
        this.next = null;
    }
}