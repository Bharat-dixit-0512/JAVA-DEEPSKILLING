SET SERVEROUTPUT ON;

CREATE OR REPLACE FUNCTION CalculateAge (
    p_DOB IN DATE
) RETURN NUMBER IS
    v_Age NUMBER;
BEGIN
    IF p_DOB IS NULL THEN
        RETURN NULL;
    END IF;

    v_Age := TRUNC(MONTHS_BETWEEN(SYSDATE, p_DOB) / 12);
    
    RETURN v_Age;
EXCEPTION
    WHEN OTHERS THEN
        DBMS_OUTPUT.PUT_LINE('Error in CalculateAge: ' || SQLERRM);
        RETURN -1;
END;
/
